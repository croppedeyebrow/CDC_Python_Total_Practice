# 파이썬의 Primitive data type(원시 데이터 타입)이란.
# 파이썬의 Primitive data type(원시 데이터 타입)은 다음과 같다.
# int, float, complex, bool, str, list, tuple, dict, set
# int : 정수
# 정수는 무한대까지 표현할 수 있다.
# int 타입은 8bit, 16bit, 32bit, 64bit 등 다양한 크기를 가질 수 있다.

# float : 실수
# float 타입은 16bit, 32bit, 64bit, 128bit 등 다양한 크기를 가질 수 있다.
# float 타입은 부동소수점 형식으로 표현된다.

# complex : 복소수
# bool : 논리값
# bool 타입은 True, False 두 가지 값을 가질 수 있다.
# str : 문자열
# str 타입은 문자열을 표현하는 타입이다.
# str 타입은 인덱싱과 슬라이싱이 가능하다.
# str 타입은 문자열 연산이 가능하다.
# str 타입은 문자열 메서드가 가능하다.
# str 타입은 문자열 포맷팅이 가능하다.

# list vs tuple vs dict
# list : 리스트는 순서가 있는 데이터 타입이다.
# list : 리스트는 데이터를 추가, 삭제, 수정이 가능하다.

# tuple은 순서가 있는 데이터 타입이다.
# tuple은 데이터를 추가, 삭제, 수정이 불가능하다.

# dict는 키-값 쌍으로 이루어진 데이터 타입이다.
# dict는 데이터를 추가, 삭제, 수정이 가능하다.
# dict는 키를 통해 데이터를 접근할 수 있다.
# dict는 키는 유일해야 하며, 변경 불가능한 타입이어야 한다.
# dict는 값은 변경 가능한 타입이어야 한다.
# dict는 키는 문자열이어야 한다.

# list : 리스트
# tuple : 튜플
# dict : 딕셔너리


# set : 집합