# 쓰레드는 스택이라는 메모리 공간을 가지고 있음. 실행의 최소단위

# Deep Copy(깊은복사) vs Shallow Copy(얕은복사)
# 얕은 복사(shallow copy) : 최상위 레벨의 객체만 새로 생성.(메모리 공간 절약)
# 얕은 복사를 한 객체의 하위 요소를 변경해보면, 최상위 레벨은 독립적이지만, 하위 레벨은 공유됨.(최상위 레벨은 안 바뀌지만, 하위 레벨은 바뀜)
# 정리하자면, 얕은 복사는 기존 객체의 내부 구조는 그대로 유지하면서, 최상위 레벨의 객체만 새로 생성함.
# 깊은 복사는 원본 객체를 이용하여, 별개의 객체를 생성함.(객체의 모든 요소가 새롭게 생성)
# 깊은 복사를 한 객체의 하위 요소를 변경해보면, 최상위 레벨과 하위 레벨 모두 독립적임.(독립적이라는건 원본 객체에 영향을 주지 않는다는 의미)
# 정리하자면, 깊은 복사는 기존 객체를 이용하여 아예 새로운 객체를 생성함.

