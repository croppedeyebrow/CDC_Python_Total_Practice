# 오버로딩(Overloading) vs 오버라이딩(Overriding)
# 클래스는 도면이라면, 메서드는 도면의 기능.
# 같은 메서드가 다른 파라메터 조합을 받는 것이 "오버로딩" == 인풋,아웃풋이 바뀜
# 상속받은 메서드를 수정하는 것이 '오버라이딩' == 인풋과 아웃풋은 동일하나, 내용(메서드)을 수정함.
# "오버로딩" :: 같은 이름을 가진 메서드들이 다양한 파라미터 조합을 받는 것
# "오버라이딩" :: 상속받은 메서드를 필요에 따라 변경하는 것.

# Process(프로세스) vs Thread(스레드)
# 프로그램이 실행되고 있는 상태 : 프로세스
